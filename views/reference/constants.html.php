<h2 id="constants">Constants</h2>
<h3 id="quarter_pi">QUARTER_PI</h3>
<h4>Description</h4>
<p>
QUARTER_PI is a mathematical constant with a value of 0.7853982.
</p>
<h3 id="half_pi">HALF_PI</h3>
<h4>Description</h4>
<p>
HALF_PI is a mathematical constant with a value of 1.57079632679489661923.
</p>
<h3 id="pi">PI</h3>
<h4>Description</h4>
<p>
PI is a mathematical constant with a value of 3.14159265358979323846.
</p>
<h3 id="two_pi">TWO_PI</h3>
<h4>Description</h4>
<p>
TWO_PI is a mathematical constant with a value of 6.28318530717958647693.
</p>
<h3 id="tau">TAU</h3>
<h4>Description</h4>
<p>
TAU is a mathematical constant with a value of 6.28318530717958647693. It is equal to <a href="#two_pi">TWO_PI.</a>
</p>
<h3 id="degrees">DEGREES</h3>
<h4>Description</h4>
<p>
DEGREES is a constant that is used with the <a href="#angleMode">angleMode()</a> function to inform PyAngelo to use degrees for the <a href="#rotate">rotate()</a> and <a href="#arc">arc()</a> functions.
</p>
<h3 id="radians">RADIANS</h3>
<h4>Description</h4>
<p>
RADIANS is a constant that is used with the <a href="#angleMode">angleMode()</a> function to inform PyAngelo to use radians for the <a href="#rotate">rotate()</a> and <a href="#arc">arc()</a> functions.
</p>
<h3 id="keys">KEY_A to KEY_Z</h3>
<h4>Description</h4>
<p>
KEY_A through to KEY_Z are constants that can be used with the isKeyPressed function to check if the A through to the Z key has been pressed. In addition there is also KEY_SPACE, KEY_ENTER, KEY_ESCAPE, KEY_DEL, KEY_BACKSPACE, KEY_TAB, KEY_LEFT, KEY_RIGHT, KEY_UP and KEY_DOWN.
</p>
