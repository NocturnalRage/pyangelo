<h2 id="variables">Variables</h2>
<h3 id="width">width</h3>
<h4>Description</h4>
<p>
This variable holds the width of the canvas. The default width of the canvas is 500 pixels and can be changed by calling the <a href="#setCanvasSize">setCanvasSize()</a> function.
</p>
<h4>Syntax</h4>
<p>width</p>
<h3 id="height">height</h3>
<h4>Description</h4>
<p>
This variable holds the height of the canvas. The default height of the canvas is 400 pixels and can be changed by calling the <a href="#setCanvasSize">setCanvasSize()</a> function.
</p>
<h4>Syntax</h4>
<p>height</p>
<h3 id="mouseX">mouseX</h3>
<h4>Description</h4>
<p>
This variable holds the current horizontal position of the mouse. The value is the number of pixels from the origin (0, 0) of the canvas.
</p>
<h4>Syntax</h4>
<p>mouseX</p>
<h3 id="mouseY">mouseY</h3>
<h4>Description</h4>
<p>
This variable holds the current vertical position of the mouse. The value is the number of pixels from the origin (0, 0) of the canvas.
</p>
<h4>Syntax</h4>
<p>mouseY</p>
